    private void btnchooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchooseActionPerformed
        // TODO add your handling code here:
        File file = new File(txtFile.getText());
        int option = fileChooser.showOpenDialog(null);
        if (option == fileChooser.APPROVE_OPTION) {
            file = fileChooser.getSelectedFile();
            txtFile.setText(String.valueOf(file));
        } else {
            JOptionPane.showMessageDialog(null, "Voce n\u00e3o selecionou nenhum diretorio.");
        }

    }//GEN-LAST:event_btnchooseActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // TODO add your handling code here:
        File file = new File(txtFile.getText());
        try {
            FileInputStream input = new FileInputStream(file);
            Cliente2 cliente = new Cliente2();
            ClienteDAO2 cdao = new ClienteDAO2();
            cliente.setNome(txtNome.getText());
            cliente.setEndereco(txtEndereco.getText());
            cliente.setFile(input);
            cdao.create(cliente);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Insert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnInserirActionPerformed

